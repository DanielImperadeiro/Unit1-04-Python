# This program displays the area and perimeter of a rectangle.


def main():
    print("For a rectangle that has the dimensions")
    print("length 5 cm and width 7 cm:\n")
    print("area = {}".format(5 * 7), "cm^2")
    print("perimeter = {}".format(2 * (5 + 7)), "cm")


if __name__ == "__main__":
    main()
